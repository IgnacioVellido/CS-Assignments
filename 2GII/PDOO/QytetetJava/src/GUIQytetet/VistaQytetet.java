    public void actualizar(Qytetet qytetet) {
        String carta   = qytetet.getCartaActual().toString(),
               casilla = qytetet.getJugadorActual().getCasillaActual().toString(),
               jugador = qytetet.getJugadorActual().toString();
        
        // Esto habr√≠a que borrarlo, no se van a mostrar las cartas del jugador
        if (qytetet.getCartaActual() == null) {
            carta = "No tienes ninguna carta";
        }
        
        //this.vistaCartaSorpresa.actualizar(carta);
        this.vistaCasilla.actualizar(casilla);
        this.vistaJugador.actualizar(jugador);
        
        this.repaint();
        this.revalidate();
    }    
    
    public void mostarCarta(String carta) {
        this.vistaCartaSorpresa.actualizar(carta);
        
        this.repaint();
        this.revalidate();
    }

